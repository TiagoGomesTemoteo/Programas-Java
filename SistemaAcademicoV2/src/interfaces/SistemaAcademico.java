    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
     
        String escolha = Menus.escolhaMenuAlunoProfessor();
      
        if(escolha.equals("1")){
            registroProcurado();
            for(Alunos alunos: listaAlunos){          
                if(alunos.getRa().equals(registroProcurado)){
                    alunos.exibirAluno(alunos);
                return;   
                }                    
            }           
            JOptionPane.showMessageDialog(null,"Aluno não encontrado :(");  
        
        }else if(escolha.equals("2")){
            registroProcurado();                               
            for(Professores professores: listaProfessores){          
                if(professores.getRegistro().equals(registroProcurado)){
                    professores.exibirProfessor(professores);
                return;    
                }                     
            }
            JOptionPane.showMessageDialog(null,"Professor não encontrado :("); 
        
        }else{
            JOptionPane.showMessageDialog(null,"Opção Inválida!");
        } 
    }//GEN-LAST:event_jBPesquisarActionPerformed

