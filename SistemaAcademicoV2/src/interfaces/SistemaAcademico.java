    public void cadastrarEndereco(){
        endereco = new Endereco(jTRua.getText(),Integer.parseInt(jTNumero.getText()),
                jCEstado.getSelectedItem().toString(),jTCidade.getText(),
                jTBairro.getText(),jTComplemento.getText());      
    }
    
    public void cadastrarAluno(){      
        alunos = new Alunos(jTRegistroAluno.getText(),jTNome.getText(),
                endereco,jTCurso.getText(),Integer.parseInt(jTTelefone.getText()),
                jCSexo.getSelectedItem().toString());   
        
    }   
    public void cadastrarProfessor(){
        professores = new Professores(jTRegistroProfessor.getText(),jTNome.getText()
                ,endereco,Integer.parseInt(jTTelefone.getText()), 
                jCSexo.getSelectedItem().toString());
        
        professores.setCursosEDisciplinas(cursosEDisciplinas);        
        cursosEDisciplinas = new HashMap();
        listaDisciplinas = new ArrayList();
        
    }   
    
    public void carregarDadosAluno(String raProcurado){       
        for(Alunos alunos: listaAlunos){          
            if(alunos.getRa().equals(raProcurado)){
                JOptionPane.showMessageDialog(null,"Aluno encontrado!"
                        + "\nPreencha o formulário com os novos dados e clique em (Salvar)");
                    jCAlunoOuProfessor.setSelectedIndex(2);
                    jCAlunoOuProfessor.setEnabled(false);                      
                    jTRegistroAluno.setText(alunos.getRa());
                    jTNome.setText(alunos.getNome());
                    jTCurso.setText(alunos.getCurso());
                    jTTelefone.setText(Integer.toString(alunos.getTelefone()));
                    jCSexo.setSelectedItem(alunos.getSexo());
                    jTRua.setText(alunos.getEndereco().getRua());
                    jTNumero.setText(Integer.toString(alunos.getEndereco().getNumero()));
                    jCEstado.setSelectedItem(alunos.getEndereco().getEstado());
                    jTCidade.setText(alunos.getEndereco().getCidade());
                    jTBairro.setText(alunos.getEndereco().getBairro());
                        
                return;   
            }                    
        }
            
        JOptionPane.showMessageDialog(null,"Aluno não encontrado :(");    
       
    }
    
    public void salvarAlterarAluno(String raProcurado){
        for(Alunos alunos: listaAlunos){          
            if(alunos.getRa().equals(raProcurado)){
                alunos.setRa(jTRegistroAluno.getText());
                alunos.setNome(jTNome.getText());
                alunos.setCurso(jTCurso.getText());
                alunos.setTelefone(Integer.parseInt(jTTelefone.getText()));
                alunos.setSexo(jCSexo.getSelectedItem().toString());
                
                endereco.setRua(jTRua.getText());
                endereco.setNumero(Integer.parseInt(jTNumero.getText()));
                endereco.setEstado(jCEstado.getSelectedItem().toString());
                endereco.setCidade(jTCidade.getText());
                endereco.setBairro(jTBairro.getText());
                
                alunos.setEndereco(endereco);                       
                  
            }                    
        }
        
        JOptionPane.showMessageDialog(null,"Dados salvos com sucesso");
    }
    
    
       
    public int pegarIndiceAluno(){ 
        registroProcurado();
            for(Alunos alunos: listaAlunos){                      
                if(alunos.getRa().equals(registroProcurado)){
                    return listaAlunos.indexOf(alunos);                  
                }                                                       
            }
            JOptionPane.showMessageDialog(null,"Aluno não encontrado :(");
        return -1;
    }
    
    public int pegarIndiceProfessor(){   
        registroProcurado();    
            for(Professores professores: listaProfessores){                      
                if(professores.getRegistro().equals(registroProcurado)){
                    return listaProfessores.indexOf(professores);                  
                }                    
            }           
            JOptionPane.showMessageDialog(null,"Professor não encontrado :(");           
        
        return -1;
    }
    
