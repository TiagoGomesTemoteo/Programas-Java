    public void adicionarCurso(){
        String curso, disciplina = "";        
        listaDisciplinas = new ArrayList();    
        curso = JOptionPane.showInputDialog("Digite o curso");
        
        do{
            disciplina = JOptionPane.showInputDialog("Digite a matéria "
                    + "\nOu digite (3) para sair");            
            if(!disciplina.equals("3")){               
                listaDisciplinas.add(disciplina);
            }  
            
        }while(!disciplina.equals("3"));       
        cursosEDisciplinas.put(curso, listaDisciplinas);
    }
    
