public class Metodos_com_retorno {

   public int mult (int n1, int n2){
   
    int result = n1 * n2 ;   
   
    return result ;
   
   }
    
   public int adicao (int n1,int n2){
   
   int result = n1 + n2 ;
   
   return result;
   
   }
   
   public  int divi (int n1, int n2 ){
   
   int result = n1 / n2 ; 
   
   return result;
       
   }

   public int sub (int n1 , int n2 ){
   
   int result = n1 - n2 ;
   
   return result;
   }

}
